def get_stock_news(ticker):
    with st.spinner("Fetching News..."):
        st.header(f'News of {ticker}')
        sn = StockNews(ticker, save_news=False)
        df_news = sn.read_rss()
        for i in range(min(len(df_news), 10)):
            st.subheader(f'News {i+1}')
            st.write("Published Date:", df_news['published'][i])
            st.write("Title:", df_news['title'][i])
            st.write("Summary:", df_news['summary'][i])
            st.write(f'Title Sentiment: {df_news["sentiment_title"][i]}')
            st.write(f'News Sentiment: {df_news["sentiment_summary"][i]}')
            st.write("------------------------------------------------------------------")

